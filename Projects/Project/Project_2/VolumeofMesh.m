function V=VolumeofMesh(O)
V=0;
for i=1:length(O.faces)
    v1=O.vertices(O.faces(i,1),:);
    v2=O.vertices(O.faces(i,2),:);
    v3=O.vertices(O.faces(i,3),:);
    V=V+...
    (-v3(1)*v2(2)*v1(3)...
    +v2(1)*v3(2)*v1(3)...
    +v3(1)*v1(2)*v2(3)...
    -v1(1)*v3(2)*v2(3)...
    -v2(1)*v1(2)*v3(3)...
    +v1(1)*v2(2)*v3(3))/6;
end
V=abs(V);